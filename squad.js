function calcTeamRating() {
    let total = 0;
    let count = 0;
    for (let i = 0; i < 11; i++) {
        if (squad[i]) {
            total += squad[i].rating;
            count++;
        }
    }
    for (let j = 0; j < 8; j++) {
        if (bench[j]) {
            total += bench[j].rating;
            count++;
        }
    }
    return count === 0 ? 0 : Math.round(total / count);
}

function getRatingStars(rating) {
    if (rating >= 90) return "★★★★★";
    if (rating >= 85) return "★★★★☆";
    if (rating >= 80) return "★★★☆☆";
    if (rating >= 70) return "★★☆☆☆";
    if (rating > 0) return "★☆☆☆☆";
    return "☆☆☆☆☆";
}

function calcTotalChemistry() {
    let total = 0;
    for (let i = 0; i < 11; i++) {
        total += calcPlayerChemistry(i);
    }
    return Math.min(100, Math.floor(total * 1.5));
}

function calcPlayerChemistry(index) {
    const card = squad[index];
    if (!card) return 0;

    // Icons always have 10 chemistry
    if (isIconCard(card)) return 10;
    
    const formation = FORMATIONS[currentFormation];
    if(!formation) return 0;
    
    const requiredRole = formation.positions[index].role;
    let posStatus = getPositionStatus(card, requiredRole);
    if(requiredRole === 'ANY' || requiredRole === 'COACH') posStatus = 'exact';
    
    let linkPoints = 0;
    let totalLinks = 0;
    
    formation.links.forEach(link => {
        if (link[0] === index) {
            totalLinks++;
            if (squad[link[1]]) linkPoints += calcLinkChemistry(card, squad[link[1]]);
        }
        if (link[1] === index) {
            totalLinks++;
            if (squad[link[0]]) linkPoints += calcLinkChemistry(card, squad[link[0]]);
        }
    });
    
    let baseChem = 0;
    if (posStatus === 'exact') baseChem = 4;
    else if (posStatus === 'secondary') baseChem = 2;
    else if (posStatus === 'wrong') return 0; // Wrong position gives 0 chemistry

    let chem = baseChem;
    if (totalLinks > 0) {
        chem += Math.min(6, Math.round((linkPoints / totalLinks) * 6));
    }
    
    if (typeof currentCoach !== 'undefined' && currentCoach) {
        const boosts = getCoachBoosts(card, currentCoach);
        chem += boosts.chem;
    }
    
    return Math.min(10, chem);
}

function isIconCard(card) {
    return card && ((card.version && card.version === 'Icono') ||
           (card.background && card.background.indexOf('Icono') !== -1));
}

function calcLinkChemistry(cardA, cardB) {
    // If either card is an Icon, minimum 1 point (orange link)
    // Green link if they share league or nationality
    if (isIconCard(cardA) || isIconCard(cardB)) {
        let iconPoints = 1; // minimum orange
        if (cardA.league === cardB.league) iconPoints = 2;
        if (cardA.nationFlag === cardB.nationFlag) iconPoints = 2;
        return iconPoints;
    }
    let points = 0;
    if (cardA.league === cardB.league) points++;
    if (cardA.teamIcon === cardB.teamIcon) points++;
    if (cardA.nationFlag === cardB.nationFlag) points++;
    return points;
}

function drawChemistryLines() {
    const svg = document.getElementById('chemistry-lines');
    if (!svg) return;
    svg.innerHTML = '';
    const formation = FORMATIONS[currentFormation];
    if (!formation || !formation.links) return;

    formation.links.forEach(link => {
        const p1 = formation.positions[link[0]];
        const p2 = formation.positions[link[1]];
        const cardA = squad[link[0]];
        const cardB = squad[link[1]];

        if (!cardA || !cardB) return;
        const points = calcLinkChemistry(cardA, cardB);
        let strokeColor = 'rgba(255,255,255,0.2)';
        if (points >= 2) strokeColor = '#00ff00'; // 2 or 3 is Green (Strong/Perfect Link)
        else if (points === 1) strokeColor = '#ffaa00'; // 1 is Orange/Yellow (Weak Link)
        else strokeColor = '#ff0000'; // 0 is Red (Dead Link)

        const line = document.createElementNS('http://www.w3.org/2000/svg', 'line');
        line.setAttribute('x1', p1.x);
        line.setAttribute('y1', p1.y);
        line.setAttribute('x2', p2.x);
        line.setAttribute('y2', p2.y);
        line.setAttribute('stroke', strokeColor);
        line.setAttribute('stroke-width', '0.25');
        svg.appendChild(line);
    });
}


