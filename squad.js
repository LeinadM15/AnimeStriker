let squads = [];
let currentSquadIndex = 0;

// References to current squad's data
let currentFormation = "4-3-3";
let squad = new Array(11).fill(null); // Pitch
let benchSquad = new Array(8).fill(null); // Bench
let currentCoach = null;

function initSquads() {
    squads = [];
    const defaultBadge = (typeof cardsDB !== 'undefined' && cardsDB.length > 0) ? [...new Set(cardsDB.map(c => c.teamIcon).filter(Boolean))][0] : '';
    for (let i = 0; i < 8; i++) {
        squads.push({
            name: `Squad ${i + 1}`,
            formation: "4-3-3",
            pitch: new Array(11).fill(null),
            bench: new Array(8).fill(null),
            coach: null,
            badge: defaultBadge,
            kit: ''
        });
    }
}

function loadCurrentSquadState() {
    const s = squads[currentSquadIndex];
    currentFormation = s.formation;
    squad = s.pitch;
    benchSquad = s.bench;
    currentCoach = s.coach;
    
    const badgeIcon = document.getElementById('badge-icon');
    if (badgeIcon) {
        if (s.badge) {
            badgeIcon.style.backgroundImage = `url('assets/${s.badge}')`;
            badgeIcon.style.backgroundColor = 'transparent';
            badgeIcon.dataset.team = s.badge;
        } else {
            const defaultBadge = (typeof cardsDB !== 'undefined' && cardsDB.length > 0) ? [...new Set(cardsDB.map(c => c.teamIcon).filter(Boolean))][0] : '';
            if (defaultBadge) {
                badgeIcon.style.backgroundImage = `url('assets/${defaultBadge}')`;
                badgeIcon.style.backgroundColor = 'transparent';
                badgeIcon.dataset.team = defaultBadge;
                s.badge = defaultBadge;
            }
        }
    }
    
    const kitIcon = document.getElementById('kit-icon');
    if (kitIcon) {
        if (s.kit) {
            const kitObj = typeof kitsDB !== 'undefined' ? kitsDB.find(k => k.id === s.kit) : null;
            if (kitObj) {
                kitIcon.style.backgroundImage = `url('${kitObj.image}')`;
                kitIcon.style.backgroundColor = 'transparent';
                kitIcon.dataset.kit = s.kit;
            }
        } else {
            kitIcon.style.backgroundImage = '';
            kitIcon.style.backgroundColor = '';
            kitIcon.dataset.kit = '';
        }
    }
}

function saveCurrentSquadState() {
    if(!squads[currentSquadIndex]) return;
    const s = squads[currentSquadIndex];
    s.formation = currentFormation;
    s.pitch = squad;
    s.bench = benchSquad;
    s.coach = currentCoach;
    const badgeIcon = document.getElementById('badge-icon');
    if (badgeIcon) s.badge = badgeIcon.dataset.team || s.badge || '';
    
    const kitIcon = document.getElementById('kit-icon');
    if (kitIcon) s.kit = kitIcon.dataset.kit || s.kit || '';
}

function loadSquad() {
    const saved = localStorage.getItem('animeSquadsData');
    if (saved) {
        try {
            squads = JSON.parse(saved);
        } catch(e) {
            initSquads();
        }
    } else {
        initSquads();
    }
    
    if (!squads || !squads[currentSquadIndex]) {
        initSquads();
    }
    
    loadCurrentSquadState();
}

function saveSquad() {
    saveCurrentSquadState();
    localStorage.setItem('animeSquadsData', JSON.stringify(squads));
}

